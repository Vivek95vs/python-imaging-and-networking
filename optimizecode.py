import pandas as pd
import numpy as np
from scipy.spatial import cKDTree
import time

start_time = time.time()

# Load CSV file
CSVstart_time = time.time()
file_path = "C:/Users/vivek.vs/PycharmProjects/pythonProject/dose_csv_files/2D_dose_matrix.csv"
df = pd.read_csv(file_path, header=None, dtype=str).iloc[1:]  # Skip first row immediately
df.reset_index(drop=True, inplace=True)
print(f"CSV load time: {time.time() - CSVstart_time:.2f} seconds")

# Clean and convert to float
convertstart_time = time.time()
df = df.applymap(lambda x: x.strip() if isinstance(x, str) else x)  # Remove spaces
df.replace("", np.nan, inplace=True)  # Replace empty strings with NaN
df = df.apply(pd.to_numeric, errors='coerce')  # Convert to float
print(f"Clean and convert time: {time.time() - convertstart_time:.2f} seconds")

# Drop rows and columns that are completely NaN
nan_drop_time = time.time()
df.dropna(how='all', axis=0, inplace=True)
df.dropna(how='all', axis=1, inplace=True)
print(f"Drop NaN rows/columns time: {time.time() - nan_drop_time:.2f} seconds")

# Fill missing values using interpolation
missingstart_time = time.time()
df.interpolate(method='linear', axis=0, inplace=True)
df.interpolate(method='linear', axis=1, inplace=True)
print(f"Fill missing values time: {time.time() - missingstart_time:.2f} seconds")

# Get center indices
centerstart_time = time.time()
center_row, center_col = (df.shape[0] - 1) // 2, (df.shape[1] - 1) // 2
print(f"Center indices calculation time: {time.time() - centerstart_time:.2f} seconds")

# Store known (X, Y, Value) points
storeknownstart_time = time.time()
valid_mask = df.notna().values
rows, cols = np.where(valid_mask)
points = np.column_stack((cols - center_col, rows - center_row))
values = df.values[valid_mask]
print(f"Store known (X, Y, Value) points time: {time.time() - storeknownstart_time:.2f} seconds")

# Generate a fine grid for interpolation
finegridstart_time = time.time()
x_min, x_max = points[:, 0].min(), points[:, 0].max()
y_min, y_max = points[:, 1].min(), points[:, 1].max()
x_new = np.arange(x_min, x_max + 0.5, 0.5)
y_new = np.arange(y_min, y_max + 0.5, 0.5)
grid_x, grid_y = np.meshgrid(x_new, y_new)
print(f"Fine grid generation time: {time.time() - finegridstart_time:.2f} seconds")

# *** Optimized Interpolation using cKDTree ***
kdtree_start_time = time.time()
tree = cKDTree(points)
_, nearest_idx = tree.query(np.column_stack((grid_x.ravel(), grid_y.ravel())), k=1)
grid_values = values[nearest_idx].reshape(grid_x.shape)
print(f"cKDTree interpolation time: {time.time() - kdtree_start_time:.2f} seconds")

# Save interpolated values with coordinate format (x, y), value
saveinterpstart_time = time.time()
coordinate_strings = [f"({x}, {y})" for x, y in zip(grid_x.ravel(), grid_y.ravel())]
interpolated_data = np.column_stack((coordinate_strings, grid_values.ravel()))
interp_df = pd.DataFrame(interpolated_data, columns=["Coordinate", "Interpolated Value"])

interp_output_path = "C:/Users/vivek.vs/PycharmProjects/pythonProject/dose_csv_files/interpolated_values.csv"
interp_df.to_csv(interp_output_path, index=False)
print(f"Save interpolated values time: {time.time() - saveinterpstart_time:.2f} seconds")
print(f"Interpolated values saved as '{interp_output_path}'")

